"""
Extension 2 — Dialogue System.

Orchestrates the full conversational loop:

    User NL query
        → IntentParser       (parse intent + slot filling)
        → InputModifier      (modify CovariateSet / horizon)
        → ChronosForecastProvider  (re-run Chronos-2)
        → VerbalizationPipeline    (generate NL response via Extension 1)
        → DialogueResponse         (format response + delta vs original)

Usage::

    system = DialogueSystem(history=history_array, covariates=cov_set)
    response = system.query("What if we removed the marketing spend covariate?")
    print(response.answer)
    print(response.modification_description)
"""

from __future__ import annotations

import logging
import sys
from dataclasses import dataclass, field
from pathlib import Path
from typing import Any, Dict, List, Optional

import numpy as np

# Ensure repo root is in path so extension_1.* imports resolve correctly
sys.path.insert(0, str(Path(__file__).resolve().parent.parent))

from extension_1.attribution.types import CovariateSet
from extension_1.pipeline import PipelineResult, VerbalizationPipeline
from extension_1.config import PipelineConfig
